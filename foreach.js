// Definir un arreglo
const miArreglo = [1, 2, 3, 4, 5];

// Utilizar forEach para iterar sobre cada elemento del arreglo
miArreglo.forEach(function(elemento) {
  console.log(elemento);
});

// También puedes usar una arrow function para hacerlo más conciso
miArreglo.forEach(elemento => console.log(elemento));
